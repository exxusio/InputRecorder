namespace InputRecorder.Models.Mouse
{
    /// <summary>
    /// Enumerates the different types of mouse events.
    /// </summary>
    internal enum MouseEventType
    {
        /// <summary>
        /// Left mouse button click.
        /// </summary>
        ClickLeft,
        /// <summary>
        /// Right mouse button click.
        /// </summary>
        ClickRight,
        /// <summary>
        /// Mouse movement.
        /// </summary>
        Move
    }
}